package E1;

public class Tesis {
    private String titulo;
    private String mencion;
    private Estudiante dEstudiante;

    public Tesis() {}

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getMencion() {
        return mencion;
    }

    public void setMencion(String mencion) {
        this.mencion = mencion;
    }

    public Estudiante getdEstudiante() {
        return dEstudiante;
    }

    public void setdEstudiante(Estudiante dEstudiante) {
        this.dEstudiante = dEstudiante;
    }

}
